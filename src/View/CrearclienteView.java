    private void crearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearMouseClicked
        try {
            if (Cliente.getText().isEmpty() || numero.getText().isEmpty() || telefono.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe ingresar datos para poder crear al cliente");
                
            }else{
                Clientes objClientes = new Clientes();
                objClientes.getId_cliente();
                objClientes.setNombre_cliente(Cliente.getText());
                objClientes.setTelefono(telefono.getText());
                objClientes.setNumero(numero.getText());
                
                String mensaje = cl.crearCliente(objClientes);
                JOptionPane.showMessageDialog(null, mensaje);
                
                Cliente.setText("");
                telefono.setText("");
                numero.setText("");
                    
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_crearMouseClicked

